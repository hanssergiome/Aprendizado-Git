
public class Calculos {
	
	public float soma (float numA, float numB) {
		
		float resultado;
		resultado = numA+numB;
		return resultado;
		
	}

	
public float subtrai (float numA, float numB) {
		
		float resultado;
		resultado = numA-numB;
		return resultado;
		
	}

public float multiplica (float numA, float numB) {
	
	float resultado;
	resultado = numA*numB;
	return resultado;
	
}

public float divide (float numA, float numB) {
	
	float resultado;
	resultado = numA/numB;
	return resultado;
	
}
	
}
